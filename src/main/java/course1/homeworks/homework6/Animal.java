package course1.homeworks.homework6;

public class Animal {
    protected int isRun;
    protected int isSwim;

    public Animal(int isRun, int isSwim) {
        this.isRun = isRun;
        this.isSwim = isSwim;
    }

    public int getIsRun() {
        return isRun;
    }

    public int getIsSwim() {
        return isSwim;
    }
}
